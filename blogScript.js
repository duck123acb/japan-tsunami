document.getElementById("toggle-going-to-spain").addEventListener("click", function() {
    if (document.getElementById("going-to-spain").style.display === "inline") {
        document.getElementById("going-to-spain").style.display = "none";
        document.getElementById("toggle-going-to-spain").innerHTML = "<strong>+</strong>";
    } else {
        document.getElementById("going-to-spain").style.display = "inline";
        document.getElementById("toggle-going-to-spain").innerHTML = "<strong>-</strong>";
    }
});

